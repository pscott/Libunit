#include "libfw.h"

char	*fw_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	k;

	i = 0;
	k = 0;
	while (s1[i])
		++i;
	while (s2[k])
	{
		s1[i + k] = s2[k];
		++k;
	}
	s1[i + k] = '\0';
	return (s1);
}
